-- ============================================================================
-- F-12b: бегунок приватности адреса
-- ----------------------------------------------------------------------------
-- ЧТО: добавляет колонку properties.public_location_id (uuid → locations.id).
--      Это уровень локации, который объект раскрывает публично (бегунок в форме).
--      NULL = показывать полный адрес (как раньше, по location_id).
-- ЗАЧЕМ: агент может скрыть точный адрес и показать соседям только до выбранного
--        уровня (но не ниже комьюнити — это enforce'ит клиент).
-- ОБРАТИМО: да (DROP COLUMN). На данные не влияет — колонка nullable, без дефолта.
-- БЕЗОПАСНО: только своя таблица properties (MrSQM), get_feed НЕ трогаем —
--            показ уровня в ленте делает клиент (отдельный запрос).
-- ============================================================================

ALTER TABLE public.properties
  ADD COLUMN IF NOT EXISTS public_location_id uuid REFERENCES public.locations(id);

COMMENT ON COLUMN public.properties.public_location_id IS
  'Уровень локации, раскрываемый публично (бегунок приватности адреса). NULL = полный адрес (location_id).';
