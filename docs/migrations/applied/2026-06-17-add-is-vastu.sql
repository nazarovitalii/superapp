-- ----------------------------------------------------------------------------
-- ЧТО: добавляет колонку properties.is_vastu (boolean, NOT NULL, DEFAULT false).
--      Показывается в карточке как «Bedrooms: 2 + maid + vastu» и в ленте суффиксом «+v».
-- ЗАЧЕМ: агент отмечает соответствие объекта Vastu (важно части клиентов).
-- ОБРАТИМО: да (DROP COLUMN). DEFAULT false → существующие 10 строк получат false,
--           на чтение/контракты не влияет.
-- БЕЗОПАСНО: только своя таблица properties (MrSQM). RLS не меняется (колонка на
--            существующей таблице, наследует политики). get_feed/get_property —
--            отдельными миграциями (возврат поля), здесь только колонка.
-- РОЛЬ: применять под supabase_admin (не postgres).
-- ============================================================================

ALTER TABLE public.properties
  ADD COLUMN IF NOT EXISTS is_vastu boolean NOT NULL DEFAULT false;

COMMENT ON COLUMN public.properties.is_vastu IS
  'Соответствие объекта Vastu. Показ: карточка «+ vastu», лента «+v». DEFAULT false.';
